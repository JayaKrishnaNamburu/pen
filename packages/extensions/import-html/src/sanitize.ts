import DOMPurify from "isomorphic-dompurify";

const PURIFY_CONFIG = {
  ALLOWED_TAGS: [
    "p",
    "br",
    "hr",
    "h1",
    "h2",
    "h3",
    "h4",
    "h5",
    "h6",
    "ul",
    "ol",
    "li",
    "a",
    "strong",
    "b",
    "em",
    "i",
    "u",
    "s",
    "del",
    "strike",
    "code",
    "pre",
    "blockquote",
    "table",
    "thead",
    "tbody",
    "tr",
    "th",
    "td",
    "img",
    "mark",
    "span",
    "div",
    "details",
    "summary",
    "input",
  ],
  ALLOWED_ATTR: [
    "href",
    "src",
    "alt",
    "title",
    "width",
    "height",
    "class",
    "id",
    "colspan",
    "rowspan",
    "type",
    "checked",
    "disabled",
    "style",
    "data-*",
    "open",
  ],
  ALLOW_DATA_ATTR: true,
  FORBID_TAGS: [
    "script",
    "style",
    "iframe",
    "object",
    "embed",
    "applet",
    "form",
    "noscript",
    "template",
    "math",
    "svg",
  ],
  FORBID_ATTR: ["onerror", "onclick", "onload", "onmouseover"],
  RETURN_TRUSTED_TYPE: false,
};

export function sanitizeHTML(html: string): string {
  return DOMPurify.sanitize(html, PURIFY_CONFIG) as string;
}
